    private void jb_ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_ingresarMouseClicked
        // TODO add your handling code here
        usuarios.add(new Usuario("prueba", "123"));
        for (int i = 0; i < usuarios.size(); i++) {
            if (usuarios.get(i).getUsuario().equals(tf_usuario.getText()) && usuarios.get(i).getContrasena().equals(pf_contrasena.getText())) {
                posicion = i;
                jd_opciones_usuarios.setModal(true);
                jd_opciones_usuarios.pack();
                jd_opciones_usuarios.setLocationRelativeTo(this);
                jd_opciones_usuarios.setVisible(true);

            } else {
                for (int j = 0; j < artistas.size(); j++) {
                    if (artistas.get(i).getUsuario().equals(tf_usuario.getText()) && artistas.get(i).getContrasena().equals(pf_contrasena.getText())) {
                        posicion = j;
                        jd_artistas.setModal(true);
                        jd_artistas.pack();
                        jd_artistas.setLocationRelativeTo(this);
                        jd_artistas.setVisible(true);
                    }
                }

            }
        }


    }//GEN-LAST:event_jb_ingresarMouseClicked

